import mongoose from "mongoose";

const RendezModel = new mongoose.Schema(
  {
    fname: {
      type: String,
      required: true,
    },
    lname: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    service: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timeseries: true }
);

const Appointment = mongoose.model("Appointment", RendezModel);

export default Appointment;
